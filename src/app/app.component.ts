  constructor(
    platform: Platform,
    statusBar: StatusBar,
    splashScreen: SplashScreen,
    sql: Storage,
    public alertCtrl: AlertController
  ) {

    sql.ready().then(() => {

      sql.get('tb_rute').then((val) => {

        if (val) {

          console.log('Data rute sudah ada !');

        } else {

          sql.set('tb_rute', JSON.stringify(this.datarute));

        }

      });

      sql.get('tb_angkot').then((val) => {

        if (val) {

          console.log('Data angkot sudah ada!');

        } else {

          sql.set('tb_angkot', JSON.stringify(this.dataangkot));

        }

      });

      sql.get('tb_realrute').then((val) => {

        if (val) {

          console.log('Data Rute Baru sudah ada!');

        } else {

          sql.set('tb_realrute', JSON.stringify(this.datarealrute));

        }

      });

    }).catch((err) => {

      console.log('SQL ERROR ', JSON.stringify(err));
      this.alertCtrl.create({
        title: 'Kesalahan Database',
        subTitle: 'Gagal menyiapkan SQLite',
        message: 'Error : ' + JSON.stringify(err)
      }).present();

    });

    platform.ready().then(() => {
      // Okay, so the platform is ready and our plugins are available.
      // Here you can do any higher level native things you might need.
      statusBar.styleDefault();
      splashScreen.hide();

    });
  }
